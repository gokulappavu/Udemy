// date-methods.js

// Date Methods
let date = new Date();

// getFullYear()
console.log(date.getFullYear()); // Output: Current year

// getMonth()
console.log(date.getMonth()); // Output: Current month (0-11)

// getDate()
console.log(date.getDate()); // Output: Current day of the month

// getDay()
console.log(date.getDay()); // Output: Current day of the week (0-6)

// getHours()
console.log(date.getHours()); // Output: Current hour

// getMinutes()
console.log(date.getMinutes()); // Output: Current minute

// getSeconds()
console.log(date.getSeconds()); // Output: Current second

// toISOString()
console.log(date.toISOString()); // Output: ISO 8601 string representation
